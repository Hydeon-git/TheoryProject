Scene::Scene() : Module()
{
	name.Create("scene");
}

// Destructor
Scene::~Scene()
{}

// Called before render is available
bool Scene::Awake()
{
	LOG("Loading Scene");
	bool ret = true;

	return ret;
}

// Called before the first frame
bool Scene::Start()
{
	// Texture Load
	groundTex = app->tex->Load("Assets/Textures/ground.png");
	fondo = app->tex->Load("Assets/Textures/background.png");

	// Play Music
	app->audio->PlayMusic("Assets/Audio/Music/landing.ogg", 80);

	return true;
}

// Called each loop iteration
bool Scene::PreUpdate()
{
	return true;
}

// Called each loop iteration
bool Scene::Update(float dt)
{
	app->render->DrawTexture(fondo, 0, -8000 + SCREEN_HEIGHT);

	return true;
}

// Called each loop iteration
bool Scene::PostUpdate()
{
	bool ret = true;
	if(app->input->GetKey(SDL_SCANCODE_ESCAPE) == KEY_DOWN)
		ret = false;

	return ret;
}

// Called before quitting
bool Scene::CleanUp()
{
	LOG("Freeing scene");

	return true;
}
